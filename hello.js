/******************************************************************************
* Requirements                                                                *
******************************************************************************/
var http = require('http');
var url  = require('url');

/******************************************************************************
* Server function                                                             *
******************************************************************************/
function start() {
    var __PORT__ = 14974;
    
    function onRequest(request, response) {
	response.writeHead(200, {'Content-Type': 'text/plain'});
	response.end('Test 2\n');
    };
    
    http.createServer(onRequest).listen(__PORT__);
};

/******************************************************************************
* Exports                                                                     *
******************************************************************************/
exports.start = start;