/******************************************************************************
* Requirements                                                                *
******************************************************************************/
var http = require('http');

/******************************************************************************
* Server function                                                             *
******************************************************************************/
function start(port,route,handle) {
    
    function onRequest(request, response) {
	route(handle,request,response);
    };
    
    http.createServer(onRequest).listen(port);
};

/******************************************************************************
* Exports                                                                     *
******************************************************************************/
exports.start = start;